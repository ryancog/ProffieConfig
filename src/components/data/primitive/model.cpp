using namespace data::prim;

bool Model::enable(bool en) {
    if (not setupEnable(en)) return false;

    doEnable(en);
    return true;
}

void Model::lock() const {
    mMutex.lock();
}

void Model::unlock() const {
    mMutex.unlock();
}

